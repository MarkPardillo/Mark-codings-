public class Multiply {
	public static void main(String[] args) {
		
		multiply(3,5);
	    multiply(4,6);
		multiply(7,8);
		
		System.out.println(multiply(3,5));
		System.out.println(multiply(4,6));
		System.out.println(multiply(7,8));
		
		
	}
	
	public static int multiply(int n1, int n2) {
	    return n1*n2;
	    		
	}
}